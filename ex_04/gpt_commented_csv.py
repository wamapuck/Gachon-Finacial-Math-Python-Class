# ==============================================================
# 1. 전체 로직 분석
#
# 이 스크립트는 로컬에 있는 CSV 파일('data.csv')을
# 한 줄씩 읽어들여 쉼표(',')로 분할(split)한 뒤
# 두 번째 필드(t[1])를 실수(float)로 변환하여
# total 변수에 누적하려는 의도로 작성됨.
#
# ==============================================================

# --------------------------------------------------------------
# 1‑1. 코드의 목적과 의도 분석
#
# - 목적: CSV 파일 형태의 데이터를 간단히 파싱(parse)하여
#   두 번째 열의 값들만 모아 합계를 계산하려 함.
# - 의도:
#   1) open(...).readline() + split(',') 방식으로 필드 분리 실습
#   2) 누적 변수(total)에 각 실수 값을 더해 최종 합을 구함
#
# --------------------------------------------------------------

# --------------------------------------------------------------
# 1‑1‑1. 세부 코드 로직 및 기능 분석
#
# 1) total = None
#    - 누적 합을 저장할 변수 초기화 (그러나 None으로 설정되어 있어 오류 발생)
#
# 2) for line in open('data.csv'):
#    - 'data.csv'를 한 줄씩 읽으며 반복
#
# 3)     t = line.split(',')
#    - 현재 줄을 쉼표 기준으로 분할해 리스트 t 생성
#
# 4)     total = total + print(float(t[1]))
#    - t[1] 항목을 float로 변환
#    - print()로 출력한 후 그 반환값(None)을 total에 더하려 시도 → 타입 오류
#
# ==============================================================
#
#  – 추신: CSV 파일(data.csv) 형식
#    * 각 행(row)은 쉼표로 구분된 필드(field)들로 이루어짐
#    * 예시:
#        id, value, description
#        1, 23.5, "sensor A"
#        2, 17.2, "sensor B"
#    * 이 코드에서는 두 번째 필드(value)가 실수형 숫자라고 가정
#
# ==============================================================

# ==== 원본 코드 ====  
total = 0

for line in open('data.csv'):
    t = line.split(',')
    total = total + float(t[1])  # print() 제거하고 float 값을 더함

print("Total:", total)  # 최종 합계를 출력
